exemplo = input("Imprimo e guardo ao mesmo tempo. ESCREVA QUALQUER COISA: ")
# no "INPUT" eu imprimo uma frase pro usuário e guardo sua resposta AO MESMO TEMPO!! :o
print(exemplo, "TÁ VENDO?! Aqui eu só imprimo uma frase pra você e mais nada.")
