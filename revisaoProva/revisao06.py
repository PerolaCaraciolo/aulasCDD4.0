# Ler um valor e escrever a mensagem "É MAIOR QUE 10!" se o valor lido for maior que 10
# Caso contrário, escrever "NÃO É MAIOR QUE 10!"

num = int(input("Digite um número: "))

if num > 10:
    print("É MAIOR QUE 10! \o/")
else:
    print("NÃO É MAIOR QUE 10! /o\\")     # Quando eu quero que o programa "esqueça" a função de determinado caracter,
                                          # é só colocar a contra-barra antes dele que ele é impresso! :D
