# Problema 01:

# Imagine que queremos imprimir todas as letras de um nome. Então, faça um programa que
# receba nome de uma pessoa e mostre letra por letra.

nome = input("Digite seu nome: ")

for c in nome:     # Tendo um texto como argumento, o "FOR" vai percorrer caractere por caractere, por isso
    print(c)       # imprime cada letra.
